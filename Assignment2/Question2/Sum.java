package Question2;

public class Sum {
    public Sum(){}

    public static int add(int i, int j){
        return i + j;
    }

    public static int add(int i, int j, int k){
        return i + j + k;
    }

    public static double add(int i, double j){
        return i + j;
    }

    public static double add(double i, int j){
        return i + j;
    }

    public static double add(double i,double j){
        return i + j;
    }

}
